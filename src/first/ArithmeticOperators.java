package first;

public class ArithmeticOperators {

	public static void main(String[] args) {
	int a = 15;
	int b = 4;
	
	System.out.println("a + b = " + (a + b));
	System.out.println(" a - b=" + (a-b));
	System.out.println(" a * b=" + (a*b));
	System.out.println(" a / b=" + (a/b));
	System.out.println(" a % b=" + (a%b));
	System.out.println(" a++=" + (a++));
	System.out.println(" ++b=" + (b++));

	double x =12.0;
	double y = 5.0;
	System.out.println("x /y=" + (x/y));
	}

}
